def main():
    # Some very simple math functions
    print('1 + 1 = ', 1 + 1)
    print('42 - 4 = ', 42 - 4)
    print('5 * 5 = ', 5 * 8)
    print('5 ** 2 (exponentiation) = ', 5 ** 2)
    print('8 / 3 = ', 8 / 3)
    print('8 // 3 (integer division) = ', 8 // 3)
    print('8 % 3 (remainder) = ', 8 % 3)

main()
